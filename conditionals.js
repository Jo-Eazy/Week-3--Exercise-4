let score = 69;
const passingGrade = 60;

if (score === 100) {
    console.log("Perfect score!");
} else if (score >= passingGrade) {
    console.log("Passed.");
} else {
    console.log("Failed.");
}

console.log("Score:", score);

console.log("Passing Grade:", passingGrade);
